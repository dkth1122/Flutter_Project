  //Îì±Í∏â Ï∂úÎ†•Ïö©
  Future<String> calculateExpertRating(String userId) async {
    // Firebase.initializeApp()Î•º Ìò∏Ï∂úÌïòÏßÄ ÏïäÍ≥† Ïù¥ÎØ∏ Ï¥àÍ∏∞ÌôîÎêòÏóàÎã§Í≥† Í∞ÄÏ†ïÌïòÍ≥† ÏßÑÌñâ
    final firestore = FirebaseFirestore.instance;

    // Calculate the total order amount for the user
    QuerySnapshot querySnapshot = await firestore
        .collection('orders')
        .where('seller', isEqualTo: userId)
        .get();

    for (QueryDocumentSnapshot document in querySnapshot.docs) {
      totalAmount += document['price'];
    }

    //Î¨∏ÏÑúÍ∞Ä Ï¥ù Î™áÍ∞ú ÏûàÎäîÏßÄ
    documentCount = querySnapshot.size;

    // Determine the expert rating based on the total order amount
    rating = 'New üå±';

    if (documentCount >= 1 || totalAmount >= 5000) {
      setState(() {
        rating = 'LEVEL 1 üçÄ';
      });
    }

    if (documentCount >= 15 || totalAmount >= 5000000) {
      setState(() {
        rating = 'LEVEL 2 üå∑';
      });
    }

    if (documentCount >= 100 || totalAmount >= 20000000) {
      setState(() {
        rating = 'LEVEL 3 üå∫';
      });
    }

    if (documentCount >= 300 || totalAmount >= 80000000) {
      setState(() {
        rating = 'MASTER üíê';
      });
    }

    DocumentReference userDocumentRef = firestore.collection('rating').doc(userId);

    if (userDocumentRef != null) {
      // Ïù¥ÎØ∏ Ìï¥Îãπ ÏÇ¨Ïö©ÏûêÏùò Î¨∏ÏÑúÍ∞Ä Ï°¥Ïû¨ÌïòÎäî Í≤ΩÏö∞, "set"ÏùÑ ÏÇ¨Ïö©ÌïòÏó¨ ÏóÖÎç∞Ïù¥Ìä∏
      userDocumentRef.set({
        'user': userId,
        'rating': rating,
      });
    } else {
      // Ìï¥Îãπ ÏÇ¨Ïö©ÏûêÏùò Î¨∏ÏÑúÍ∞Ä ÏóÜÎäî Í≤ΩÏö∞, "add"Î•º ÏÇ¨Ïö©ÌïòÏó¨ ÏÉàÎ°úÏö¥ Î¨∏ÏÑú Ï∂îÍ∞Ä
      firestore.collection('rating').add({
        'user': userId,
        'rating': rating,
      });
    }
    return rating;
  }

